"""Guard spec test files from direct modification (PreToolUse hook).

Spec tests are generated by /codify-spec and reviewed by /review-spec-tests.
They should not be hand-edited during implementation. Outputs permissionDecision
"ask" so the user can override when intentional (e.g., /update-spec workflow).
"""

import json
import re
import sys

EDIT_TOOLS = {"Edit", "Write", "MultiEdit", "NotebookEdit"}

PROTECTED_PATTERNS = [
    r"(^|/)spec_tests/",          # anything in a spec_tests/ directory
    r"(^|/)spec_[^/]+_test\.",    # spec_foo_test.py, spec_auth_test.go
    r"(^|/)test_spec_[^/]+\.",    # test_spec_foo.py, test_spec_auth.py
]


def get_file_path(data: dict) -> str | None:
    tool_input = data.get("tool_input", {})
    return tool_input.get("file_path") or tool_input.get("path")


def main():
    try:
        data = json.load(sys.stdin)
    except (json.JSONDecodeError, EOFError):
        sys.exit(0)

    if data.get("tool_name") not in EDIT_TOOLS:
        sys.exit(0)

    file_path = get_file_path(data)
    if not file_path:
        sys.exit(0)

    for pattern in PROTECTED_PATTERNS:
        if re.search(pattern, file_path):
            basename = file_path.rsplit("/", 1)[-1] if "/" in file_path else file_path
            result = {
                "hookSpecificOutput": {
                    "hookEventName": "PreToolUse",
                    "permissionDecision": "ask",
                    "permissionDecisionReason": (
                        f"Modifying spec test '{basename}' — "
                        "spec tests are generated by /codify-spec, not hand-edited."
                    ),
                }
            }
            json.dump(result, sys.stdout)
            sys.exit(0)


if __name__ == "__main__":
    main()
